#Initializes Global Parameters
.\Global-Config

<#
Exposed parameters for creating the Processing Role, so you can change it for production
#>
$Path = "$ConfigRoot\sitecore-XP1-prc.json"
$Package = "$($PackageRoot)_prc.scwdp.zip"
$SiteName = "$Prefix.processing" #The website that will be created in IIS

$SSLCert = "" #Todo: needs to be provided (applicable for production environment), if not then generated by the script (applicable for development environment).
$XConnectCert = "$Prefix.xconnect_client" 

#Configure Processing role
$ProcessingParams = @{     
    Path = $Path
    Package = $Package     
    LicenseFile = $LicenseFile   
    XConnectCert = $XConnectCert  
    SSLCert = $SSLCert
    SqlDbPrefix = $Prefix
    SolrCorePrefix = $SolrCorePrefix
    SiteName = $SiteName
    SqlAdminUser = $SqlAdminUser
    SqlAdminPassword = $SqlAdminPassword
    SqlCoreUser = $SqlCoreUser
    SqlCorePassword = $SqlCorePassword
    SqlMasterUser = $SqlMasterUser
    SqlMasterPassword = $SqlMasterPassword
    SqlWebUser = $SqlWebUser
    SqlWebPassword = $SqlWebPassword
    SqlReportingUser = $SqlReportingUser
    SqlReportingPassword = $SqlReportingPassword
    SqlReferenceDataUser = $SqlReferenceDataUser
    SqlReferenceDataPassword = $SqlReferenceDataPassword
    SqlProcessingPoolsUser = $SqlProcessingPoolsUser
    SqlProcessingPoolsPassword = $SqlProcessingPoolsPassword
    SqlProcessingTasksUser = $SqlProcessingTasksUser
    SqlProcessingTasksPassword = $SqlProcessingTasksPassword
    SqlServer = $SqlServer
    SolrUrl = $SolrUrl
    XConnectCollectionService  = $XConnectCollectionService
    ReportingServiceApiKey = $ReportingServiceApiKey
} 
Write-Host @ProcessingParams
Install-SitecoreConfiguration @ProcessingParams

